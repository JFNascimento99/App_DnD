export function modif(atributo){

    if(atributo <= 1){return -5}

    else if(atributo == 2 || atributo == 3){return -4}

    else if(atributo == 4 || atributo == 5){return -3}

    else if(atributo == 6 || atributo == 7){return -2}

    else if(atributo == 8 || atributo == 9){return -1}

    else if(atributo == 10 || atributo == 11){return +0}

    else if(atributo == 12 || atributo == 13){return +1}

    else if(atributo == 14 || atributo == 15){return +2}

    else if(atributo == 16 || atributo == 17){return +3}

    else if(atributo == 18 || atributo == 19){return +4}

    else if(atributo == 20 || atributo == 21){return +5}

    else if(atributo == 22 || atributo == 23){return +6}

    else if(atributo == 24 || atributo == 25){return +7}

    else if(atributo == 26 || atributo == 27){return +8}
    
    else if(atributo == 28 || atributo == 29){return +9}

    else if(atributo >= 30){return +10}
    

    return +0;
}